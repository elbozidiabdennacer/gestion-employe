    public void getTemp(){
    
        TempsEmployee tmpServ = new TempsEmployee();
        ArrayList<GesTemps> tmpEmp = tmpServ.getTable(idcli);
        
        DefaultTableModel mod= (DefaultTableModel)tableTemp.getModel();
        Object[] row = new Object[3];
        mod.setRowCount(0);
        for (int i=0; i<tmpEmp.size();i++){
      
            row[0] = tmpEmp.get(i).getTempsEntry();
            row[1] = tmpEmp.get(i).getTempsSortie();
            row[2] = tmpEmp.get(i).getDate();
            mod.addRow(row);
       
        }
    }
    
    public void getTabSal(){
    
        SalEmployee salServ = new SalEmployee();
        ArrayList<GesSalaire> salEmp = salServ.getListSal(idcli);
        
        DefaultTableModel mod2= (DefaultTableModel)tabsalairs.getModel();
        Object[] row2 = new Object[4];
        mod2.setRowCount(0);
        for (int i=0; i<salEmp.size();i++){
      
            row2[0] = salEmp.get(i).getSalaire();
            row2[1] = salEmp.get(i).getPaiement();
            row2[2] = salEmp.get(i).getReste();
            row2[3] = salEmp.get(i).getDateModif();
            mod2.addRow(row2);
       
        }
    }
    
